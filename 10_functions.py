# Functions ✔

# Para crear una función se utiliza para iniciar la palabra reservada "def"

def hola():
    print("Hola") 

# Despues se invoca la función para que se inicie su ejecucion

hola()

# Una función tambien puede recibir parametros o argumentos para trabajar con ellos

def is_a_sum(a, b):
    my_sum = a + b
    return my_sum

# Despues la invocamos o llamamos pasandole los parametros o argumentos que necesita para trabajar

is_a_sum(4, 10)

