# While


one_condition = True
id = 1

# Esto generaria un bucle infinito por que one_condition es siempre verdad
while one_condition:
    print(id)
    id += 1
    if id == 7: # Añadiendo esta linea el bucle se detendria cuando el id valga 7
        break


other_condition = 20

while other_condition < 30:
    print("Sigo siendo menor que 20")
    other_condition += 1


